// if文による条件分岐
let number = 1;
if (number === 1) {
    console.log("1だね")　//条件式が成立した時の処理
} else {
    console.log("1じゃないね")　//条件式が成立しなかったときの処理
}

// if文による条件分岐 else if
let number2 = 3;
if (number2 === 1) {
    console.log("1だね");　//条件式が成立した時の処理
} else if (number2 === 2) {
    console.log("2だね");　//条件式２が成立した時の処理
} else {
    console.log("1でも2でもないね");　//条件式が成立しなかったときの処理
}